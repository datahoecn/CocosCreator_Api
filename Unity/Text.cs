Font Style 			字体风格，有Normal（普通）、Bold（黑体）、Italic（斜体）、Bold And Italic（斜黑体）4种
Line Spacing 		行距
Rich Text 			是否使用富态字
Alignment 			文本对齐方式
Horizontal Overflow 水平溢出，文本显示不受Text元素的宽度限制。超出的字不显示
Vertical Overflow 	垂直溢出，文本显示不受Text元素的高度限制，垂直超出的行不显示
Best Fit 			是否忽略字体大小，而将文本全部显示在元素区域内
Color 				字体的颜色
Material 			渲染文本所使用的材质，默认留空即可

