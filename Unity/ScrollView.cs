	Movement Type 			内容移动的模式
		Unrestricted，不限制，可以移动到RectTransform所设定的区域外；
		Clamped，限制区域，不能移动出去；
		Elastic，限制区域，移动出去会弹回来
	Elasticity 				Movement Type为Elastic时，当出界时弹回的力度
	Intertia 				惯性。当勾选惯性时，在停止拖动后，内容将逐渐减缓移动最后停止；不勾选惯性时，在停止拖动后，内容会立刻停止
	Deceleration Rate 		勾选惯性时，停止拖曳后的减速度
	Scroll Sensitivity 		拖曳的敏感度